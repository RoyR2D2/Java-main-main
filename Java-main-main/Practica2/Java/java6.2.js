var primerNumero;
var segundoNumero;
primerNumero = prompt("Introduzca el primer numero");
segundoNumero = prompt("Introduzca el segundo numero");

document.writeln("La suma es " + primerNumero + segundoNumero);
document.writeln("La resta es " + (parseInt(primerNumero) - parseInt(segundoNumero)));
document.writeln("La multiplicacion es " + primerNumero * segundoNumero);
document.writeln("El cociente es " + primerNumero / segundoNumero);
document.writeln("El resto es " + primerNumero % segundoNumero); 